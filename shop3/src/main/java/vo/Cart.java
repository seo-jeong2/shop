package vo;

public class Cart {
	private int goodsNo;
	private String customerId;
	private int cartQuanitity;
	private int cartTotalnum;
	private int cartTotalprice;
	private String updateDate;
	private String createDate;
	
	public int getGoodsNo() {
		return goodsNo;
	}
	public void setGoodsNo(int goodsNo) {
		this.goodsNo = goodsNo;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public int getCartQuanitity() {
		return cartQuanitity;
	}
	public void setCartQuanitity(int cartQuanitity) {
		this.cartQuanitity = cartQuanitity;
	}
	public int getCartTotalnum() {
		return cartTotalnum;
	}
	public void setCartTotalnum(int cartTotalnum) {
		this.cartTotalnum = cartTotalnum;
	}
	public int getCartTotalprice() {
		return cartTotalprice;
	}
	public void setCartTotalprice(int cartTotalprice) {
		this.cartTotalprice = cartTotalprice;
	}
	public String getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	
	
}
